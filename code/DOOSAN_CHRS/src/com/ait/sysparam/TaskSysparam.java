
package com.ait.sysparam;
/**
 * 定时任务参数对象
 * 
 * 更改后需要重新启动 WS
 * @version 1.0
 */
public class TaskSysparam extends Sysparam{

    private final static String default_paramName = "定时任务 参数设置";
    private final static String default_description = "定时任务 参数设置";
  
    public void initialized(){
    	this.setParamName(default_paramName);
    	this.setDescription(default_description);
    }

	public static String getDefault_paramName() {
		return default_paramName;
	}

	public static String getDefault_description() {
		return default_description;
	}
	
	/**
	 * 生成决裁mail 毫秒
	 * -1:不执行 发送任务
	 */
	private int affirmMailDelay = 0;
	
	/**
	 * 生成决裁mail 间隔时间 毫秒
	 * -1:不检查
	 */
	private int affirmMailPeriod = 0;
	
	/**
	 * erp数据传输任务 毫秒
	 * -1:不执行 发送任务
	 */
	private int erpDelay = 0;
	
	/**
	 * erp数据传输任务 间隔时间 毫秒
	 * -1:不检查
	 */
	private int erpPeriod = 0;

	/**
	 * Eatery数据传输任务 毫秒
	 * -1:不执行 发送任务
	 */
	private int eateryDelay = 0;
	
	/**
	 * Eatery数据传输任务 间隔时间 毫秒
	 * -1:不检查
	 */
	private int eateryPeriod = 0;
	
	/**
	 * 发送邮件任务 延迟时间 毫秒
	 * -1:不执行 email 发送任务
	 */
	private int mailDelay = 0;
	
	/**
	 * 发送邮件任务 间隔时间 毫秒
	 * -1:不检查
	 */
	private int mailPeriod = 0;

	
	/**
	 * 部门备份任务 延迟时间 毫秒
	 * -1:不执行 email 发送任务
	 */
	private int deptDelay = 0;
	
	/**
	 * 部门备份任务 间隔时间 毫秒
	 * -1:不检查
	 */
	private int deptPeriod =0;

	/**
	 * 刷卡读取任务 延迟时间 毫秒
	 * -1:不执行 email 发送任务
	 */
	private int cardDelay = 0;
	
	/**
	 * 刷卡读取任务 间隔时间 毫秒
	 * -1:不检查
	 */
	private int cardPeriod = 0;

	public int getCardDelay() {
		return cardDelay;
	}

	public void setCardDelay(int cardDelay) {
		this.cardDelay = cardDelay;
	}

	public int getCardPeriod() {
		return cardPeriod;
	}

	public void setCardPeriod(int cardPeriod) {
		this.cardPeriod = cardPeriod;
	}

	public int getDeptDelay() {
		return deptDelay;
	}

	public void setDeptDelay(int deptDelay) {
		this.deptDelay = deptDelay;
	}

	public int getDeptPeriod() {
		return deptPeriod;
	}

	public void setDeptPeriod(int deptPeriod) {
		this.deptPeriod = deptPeriod;
	}

	public int getMailDelay() {
		return mailDelay;
	}

	public void setMailDelay(int mailDelay) {  
		this.mailDelay = mailDelay;
	}

	public int getMailPeriod() {
		return mailPeriod;
	}

	public void setMailPeriod(int mailPeriod) {
		this.mailPeriod = mailPeriod;
	}

	public int getErpDelay() {  
		return erpDelay;
	}

	public void setErpDelay(int erpDelay) {
		this.erpDelay = erpDelay;
	}
                             
	public int getErpPeriod() {
		return erpPeriod;
	}

	public void setErpPeriod(int erpPeriod) {
		this.erpPeriod = erpPeriod;
	}

	
	public int getEateryDelay() {
		return eateryDelay;
	}

	
	public void setEateryDelay(int eateryDelay) {
		this.eateryDelay = eateryDelay;
	}

	
	public int getEateryPeriod() {
		return eateryPeriod;
	}

	
	public void setEateryPeriod(int eateryPeriod) {
		this.eateryPeriod = eateryPeriod;
	}

	
	public int getAffirmMailDelay() {
		return affirmMailDelay;
	}

	
	public void setAffirmMailDelay(int affirmMailDelay) {
		this.affirmMailDelay = affirmMailDelay;
	}

	
	public int getAffirmMailPeriod() {
		return affirmMailPeriod;
	}

	
	public void setAffirmMailPeriod(int affirmMailPeriod) {
		this.affirmMailPeriod = affirmMailPeriod;
	}

	
}
