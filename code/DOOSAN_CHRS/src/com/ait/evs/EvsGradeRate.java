package com.ait.evs;

public class EvsGradeRate {

    private int empGradeCount;

    private String evGrade;
    
    private String evGradeName;
    
    private float rate;

    private float residual;

    public EvsGradeRate() {
    }

    public EvsGradeRate(String evGrade, int empGradeCount, float residual,
            float rate) {
        this.evGrade = evGrade;
        this.empGradeCount = empGradeCount;
        this.residual = residual;
        this.rate = rate;
    }

    public int getEmpGradeCount() {
        return empGradeCount;
    }

    public String getEvGrade() {
        return evGrade;
    }

    public float getRate() {
        return rate;
    }

    public float getResidual() {
        return residual;
    }

    public void setEmpGradeCount(int empGradeCount) {
        this.empGradeCount = empGradeCount;
    }

    public void setEvGrade(String evGrade) {
        this.evGrade = evGrade;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }

    public void setResidual(float residual) {
        this.residual = residual;
    }
    
    
    /**
     * @return 返回 evGradeName。
     */
    public String getEvGradeName() {
        return evGradeName;
    }
    /**
     * @param evGradeName 要设置的 evGradeName。
     */
    public void setEvGradeName(String evGradeName) {
        this.evGradeName = evGradeName;
    }
}