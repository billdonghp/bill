package com.ait.ar.bean;

public class EmpRoll {
  private String empid="";
  private String ar_date="";
  private String leave_type_code="";
  private String leave_time="";
  private String LATE_TIME="";
  private String EARLY_TIME="";
  private String typeName;
  private String LEAVE_REASON;
  public EmpRoll() {
  }
  public String getEmpid() {
    return empid;
  }
  public void setEmpid(String empid) {
    this.empid = empid;
  }
  public String getAr_date() {
    return ar_date;
  }
  public void setAr_date(String ar_date) {
    this.ar_date = ar_date;
  }
  public String getLeave_type_code() {
    return leave_type_code;
  }
  public void setLeave_type_code(String leave_type_code) {
    this.leave_type_code = leave_type_code;
  }
  public String getLeave_time() {
    return leave_time;
  }
  public void setLeave_time(String leave_time) {
    this.leave_time = leave_time;
  }
  public String getLATE_TIME() {
    return LATE_TIME;
  }
  public void setLATE_TIME(String LATE_TIME) {
    this.LATE_TIME = LATE_TIME;
  }
  public String getEARLY_TIME() {
    return EARLY_TIME;
  }
  public void setEARLY_TIME(String EARLY_TIME) {
    this.EARLY_TIME = EARLY_TIME;
  }
  public String getTypeName() {
    return typeName;
  }
  public void setTypeName(String typeName) {
    this.typeName = typeName;
  }
  public String getLEAVE_REASON() {
    return LEAVE_REASON;
  }
  public void setLEAVE_REASON(String LEAVE_REASON) {
    this.LEAVE_REASON = LEAVE_REASON;
  }
}
