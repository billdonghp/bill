package com.ait.utils;

public class ToolMenu {
  private int select;
  private int update;
  private int insertr;
  private int delect;
  public ToolMenu() {
  }
  public int getSelect() {
    return select;
  }
  public void setSelect(int select) {
    this.select = select;
  }
  public int getUpdate() {
    return update;
  }
  public void setUpdate(int update) {
    this.update = update;
  }
  public int getInsertr() {
    return insertr;
  }
  public void setInsertr(int insertr) {
    this.insertr = insertr;
  }
  public int getDelect() {
    return delect;
  }
  public void setDelect(int delect) {
    this.delect = delect;
  }
}
