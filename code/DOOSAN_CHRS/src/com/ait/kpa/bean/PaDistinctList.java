package com.ait.kpa.bean;

public class PaDistinctList {
	
	private String fieldName;
	
	private String fieldEnName;
	
	private String fieldKorName;
	
	private String distinctField;
	
	private String tableName;
	
	private int id;
	
	private int activity;

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public String getFieldEnName() {
		return fieldEnName;
	}

	public void setFieldEnName(String fieldEnName) {
		this.fieldEnName = fieldEnName;
	}

	public String getFieldKorName() {
		return fieldKorName;
	}

	public void setFieldKorName(String fieldKorName) {
		this.fieldKorName = fieldKorName;
	}

	public String getDistinctField() {
		return distinctField;
	}

	public void setDistinctField(String distinctField) {
		this.distinctField = distinctField;
	}

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getActivity() {
		return activity;
	}

	public void setActivity(int activity) {
		this.activity = activity;
	}
	
}
