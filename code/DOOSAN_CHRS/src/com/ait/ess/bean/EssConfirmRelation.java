package com.ait.ess.bean;

public class EssConfirmRelation {
  private int confirmNo;
  private String  empID;
  private String  chinesename;
  private String  confirmorId;
  private String  confirmorName;
  public EssConfirmRelation() {
  }
  public String getChinesename() {
    return chinesename;
  }
  public int getConfirmNo() {
    return confirmNo;
  }
  public String getConfirmorId() {
    return confirmorId;
  }
  public String getConfirmorName() {
    return confirmorName;
  }
  public String getEmpID() {
    return empID;
  }
  public void setChinesename(String chinesename) {
    this.chinesename = chinesename;
  }
  public void setConfirmNo(int confirmNo) {
    this.confirmNo = confirmNo;
  }
  public void setConfirmorId(String confirmorId) {
    this.confirmorId = confirmorId;
  }
  public void setConfirmorName(String confirmorName) {
    this.confirmorName = confirmorName;
  }
  public void setEmpID(String empID) {
    this.empID = empID;
  }
}
